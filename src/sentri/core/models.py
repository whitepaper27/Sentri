@dataclass
class DatabaseProfile:
    """Output of Agent 0 (Profiler). JSON-serializable DB profile."""

    database_id: str
    db_type: str = ""  # OLTP, OLAP, MIXED
    db_size_gb: float = 0.0
    omf_enabled: bool = False
    is_rac: bool = False
    is_cdb: bool = False
    con_id: int = 0  # Container ID: 0/1=CDB$ROOT, >1=PDB
    critical_parameters: dict = field(default_factory=dict)
    workload_metrics: dict = field(default_factory=dict)
    risk_areas: list = field(default_factory=list)
    non_standard_params: dict = field(default_factory=dict)
    db_config: dict = field(default_factory=dict)  # Raw discovery data for LLM
    profiled_at: str = field(default_factory=_now_iso)
    version: int = 1

    def to_json(self) -> str:
        return json.dumps(asdict(self), cls=_OracleJSONEncoder)

    @classmethod
    def from_json(cls, s: str) -> DatabaseProfile:
        return cls(**json.loads(s))


