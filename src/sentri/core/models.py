"""Domain models for the Sentri system."""

from __future__ import annotations

import json
import uuid
from dataclasses import asdict, dataclass, field
from datetime import date, datetime, timezone
from decimal import Decimal
from typing import Optional


class _OracleJSONEncoder(json.JSONEncoder):
    """Handle Oracle types that aren't JSON-serializable by default."""

    def default(self, obj):
        if isinstance(obj, datetime):
            return obj.isoformat()
        if isinstance(obj, date):
            return obj.isoformat()
        if isinstance(obj, Decimal):
            return float(obj)
        return super().default(obj)


def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _new_id() -> str:
    return str(uuid.uuid4())


@dataclass
class Suggestion:
    """Output of Agent 1 (Scout). A parsed email alert."""

    alert_type: str
    database_id: str
    raw_email_subject: str
    raw_email_body: str
    extracted_data: dict
    confidence: float = 1.0
    parsed_at: str = field(default_factory=_now_iso)

    def to_json(self) -> str:
        return json.dumps(asdict(self))

    @classmethod
    def from_json(cls, s: str) -> Suggestion:
        return cls(**json.loads(s))


